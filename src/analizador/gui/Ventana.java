      /**
     * Método utilizado para remover todos los elementos de la tabla de la
     * interfaz principal
     */
    public void limpiarTabla() {
        DefaultTableModel limpiar = (DefaultTableModel) tabla.getModel();
        int rowCount = limpiar.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            limpiar.removeRow(i);
        }
        tabla.setModel(limpiar);
    }
    

