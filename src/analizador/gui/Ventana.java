    private void jButtonAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnalizarActionPerformed
          if (logica.getRutaArchivo() == null) {
               jTxtAreaErrores.setText("Error al analizar: \n No se ha guardado el archivo \n Primero debe guardar el archivo y luego analizar");
              
        } else if(txaCodigo.getText().equals("")){
            jTxtAreaErrores.setText("No hay codigo Para Analizar \n Por favor Escriba su codigo");
            
        }else {
           logica.guardar(txaCodigo.getText());
           jTxtAreaErrores.setText("");
           jTxtAreaMensajes.setText("Guardado");
            limpiarTabla();
            logica.getTokens().clear();
           logica.obtenerTokens(txaCodigo.getText(), 1);
        }
    }//GEN-LAST:event_jButtonAnalizarActionPerformed
